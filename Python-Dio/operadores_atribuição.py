# atriuição simples =
# atribuição com soma += (saldo = 500 --> saldo = saldo + 500)
# atribuição com subtração -= (saldo = 500 --> saldo = saldo - 500)
# atribuição com multiplicação *= (saldo = 500 --> saldo = saldo * 500)
# atribuição com divisao /= (saldo = 500 --> saldo = saldo / 500)
# atribuição com div int //= (saldo = 500 --> saldo = saldo // 500)
# atribuição com modulo %= (saldo = 500 --> saldo = saldo % 500)
# atribuição com exponeciação **= (saldo = 500 --> saldo = saldo ** 500)

saldo = 500
saldo += 100
print(saldo)

saldo = 700
saldo -= 400
print(saldo)


saldo = 90
saldo *= 2
print(saldo)

saldo = 10
saldo /= 5
print(saldo)

saldo = 5
saldo //= 2
print(saldo)

saldo = 8
saldo **= 3
print(saldo)

saldo = 30
saldo %= 5
print(saldo)